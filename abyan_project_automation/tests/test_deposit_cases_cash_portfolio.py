def test_deposit_in_primary_cash_category_using_mada_and_hyperpay():
    """Verify successfully deposit in "Primary Cash" using payment method Mada with payment gateway 'Hyperpay'."""
    driver = create_driver()
    login_page = LoginPage(driver)
    login_page.click_welcomescreen_next_buttons()
    login_page.select_user()
    login_page.enter_phone_number("584679799")
    login_page.click_continue()
    login_page.enter_password(password)
    # login_page.enter_otp_code()
    login_page.verify_holistic_home_screen()
    cash_deposit_page = DepositCashPortfolio(driver)
    cash_deposit_page.click_on_pre_define_category_and_verify_home_screen("مدخراتك")
    cash_deposit_page.verify_cash_portfolio_home_screen()
    cash_deposit_page.perform_click_action_on_deposit_button_and_navigate_next_screen()
    cash_deposit_page.perform_click_action_on_mada_payment_card()
    cash_deposit_page.perform_click_action_on_amount_input_field()
    cash_deposit_page.input_deposit_amount(1000)
    cash_deposit_page.perform_click_action_on_view_screen()
    cash_deposit_page.perform_click_on_deposit_button()
    cash_deposit_page.enter_cvv_code()
    cash_deposit_page.click_on_screen_view_and_close_the_keyboard()
    cash_deposit_page.click_on_add_card_button()
    cash_deposit_page.click_on_browser_success_pay_button()
    cash_deposit_page.perform_click_action_to_close_the_deposit_screen()
    # cash_deposit_page.perform_click_action_to_close_the_recurring_pop_up()
    cash_deposit_page.perform_scroll_down_and_click_first_transaction()
    cash_deposit_page.click_on_topmost_deposit_transaction()
    cash_deposit_page.verify_title_of_transection_detail_screen()
    cash_deposit_page.perform_scroll_down_on_screen()
    cash_deposit_page.click_back_button_to_move_primary_portfolio_home_screen()
    cash_deposit_page.click_back_button_to_move_holistic_screen()
    cash_deposit_page.perform_pull_down_to_refresh_the_screen()
    cash_deposit_page.click_on_pre_define_category_and_verify_home_screen("مدخراتك")

